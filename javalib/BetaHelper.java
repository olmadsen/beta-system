package beta;

public class BetaHelper
{
    // Instance methods 
    public long convertInt2long(int i){
	return (long)i;
    }
    public int  convertLong2int(long j){
	return (int)j;
    }

    // static methods
    public static long int2long(int i){
	return (long)i;
    }
    public static int  long2int(long j){
	return (int)j;
    }

}
